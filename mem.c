#include "chip8.h"
#include <stdio.h>
#include <string.h>

const unsigned int ROM_OFFSET = 0x200;
static const unsigned int MAX_ROM_SZ = 0xD00;
void chip8_load_rom(const char *filename)
{
    FILE *rom_file = fopen(filename, "rb");
    if (rom_file == NULL)
    {
        fprintf(stderr, "Could not open ROM file '%s'\n", filename);
        exit(1);
    }

    fseek(rom_file, 0L, SEEK_END);
    size_t rom_sz = ftell(rom_file);
    fseek(rom_file, 0L, SEEK_SET);
    
    if(rom_sz > MAX_ROM_SZ)
    {
        fprintf(stderr, "ROM '%s' of size 0x%x bytes exceeds max size of 0x%x\n", filename, rom_sz, MAX_ROM_SZ);
        exit(1);
    }

    fread(g_chip8_data.mem + ROM_OFFSET, rom_sz, 1, rom_file);

    fclose(rom_file);
}


